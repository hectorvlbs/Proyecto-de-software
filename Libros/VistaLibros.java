    private void ButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonAddActionPerformed
        DefaultTableModel model = (DefaultTableModel) SaleTable.getModel();
        int cantity = Integer.parseInt(cbCantidad.getSelectedItem().toString());
        float price = Float.parseFloat(lblPrecio.getText()) * cantity;
        model.addRow(new Object[]{idBook, lblTitulo.getText(), cantity, price});
        total += price;
        if (cantity > 1) {
            for (int i = 0; i < cantity; i++) {
                books.add(idBook);
            }
        } else {
            books.add(idBook);
        }
        lblTitulo.setText("");
        lblEdicion.setText("");
        lblPrecio.setText("");
        lblAutor.setText("");
        lblEditorial.setText("");
        lblCategoria.setText("");
        ButtonAdd.setEnabled(false);
    }//GEN-LAST:event_ButtonAddActionPerformed

