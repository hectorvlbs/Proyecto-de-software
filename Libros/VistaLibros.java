    private void ButtonConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonConfirmActionPerformed
        MySQL objectMySQL = new MySQL();
        try {
            System.out.println("total " + total);
            int idVenta = objectMySQL.ProcedureRegisterVenta(total);
            System.out.println("id " + idVenta);
            if (idVenta > 0) {
                for (int i = 0; i < books.size(); i++) {
                    objectMySQL.ProcedureRegisterVentaLibro(idVenta, books.get(i));
                }
                JOptionPane.showMessageDialog(this, "Compra registrada!", "Compra", JOptionPane.INFORMATION_MESSAGE);
                lblTotal.setText("");
                TextAreaMoney.setText("");
                lblCambio.setText("");                
                ButtonCancel.setEnabled(false);
                ButtonConfirm.setEnabled(false);
                int rowCount = SaleTable.getModel().getRowCount();
                for (int i = 0; i < rowCount; i++) {
                    DefaultTableModel model = (DefaultTableModel) SaleTable.getModel();
                    model.removeRow(i);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Error al registrar la compra", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al registrar la compra", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_ButtonConfirmActionPerformed

