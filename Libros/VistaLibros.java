    private void ButtonSaleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonSaleActionPerformed

        DocumentListener documentListener = new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) {
                validateSale();
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                validateSale();
            }

            @Override
            public void changedUpdate(DocumentEvent e) {

            }

            private void validateSale() {
                try {
                    money = Float.parseFloat(TextAreaMoney.getText());
                    if (money >= total) {
                        ButtonConfirm.setEnabled(true);
                        lblCambio.setText("$" + String.valueOf(money - total));
                    } else {
                        lblCambio.setText("Restan $" + String.valueOf(
                                (money - total) * -1));
                        ButtonConfirm.setEnabled(false);
                    }
                } catch (Exception e) {
                    lblCambio.setText("Restan $" + String.valueOf(
                            (money - total) * -1));
                }

            }
        };

        if (SaleTable.getModel().getRowCount() > 0) {
            ButtonCancel.setEnabled(true);
            lblTotal.setText(String.valueOf(total));
            TextAreaMoney.getDocument().addDocumentListener(documentListener);
        } else {
            JOptionPane.showMessageDialog(this, "No hay artículos",
                    "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_ButtonSaleActionPerformed

